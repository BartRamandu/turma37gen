package ClassesPOO;

public class Aviao {
	
	private String modelo;
	private int numeracao;
	private String cor;
	private String comapanhia;
	private String paísOrigem;
	private String horaVoo;
	
	public String getHoraVoo() {
		return horaVoo;
	}
	public void setHoraVoo(String horaVoo) {
		this.horaVoo = horaVoo;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public int getNumeracao() {
		return numeracao;
	}
	public void setNumeracao(int numeracao) {
		this.numeracao = numeracao;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getComapanhia() {
		return comapanhia;
	}
	public void setComapanhia(String comapanhia) {
		this.comapanhia = comapanhia;
	}
	public String getPaísOrigem() {
		return paísOrigem;
	}
	public void setPaísOrigem(String paísOrigem) {
		this.paísOrigem = paísOrigem;
	}
	
	
	
}
